def process_summa(number_1: int, number_2: int) -> int:
    result = number_1 + number_2
    return result


def process_devision(devidend: int, *, devisor: int) -> float:
    if not devisor:
        pass
    quotient = devidend / devisor
    return quotient


def process_substraction(number_1: int, number_2: int) -> int:
    result = number_1 + number_2
    return result


def process_mult(number_1: int, number_2: int) -> int:
    result = number_1 + number_2
    return result
